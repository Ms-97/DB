CREATE OR REPLACE FUNCTION FN_PRODNM(P_PROD_LGU IN VARCHAR2)
    RETURN VARCHAR2
IS
    V_NAME VARCHAR2(40); 
BEGIN    
    SELECT LPROD_NM INTO V_NAME     
      FROM LPROD
     WHERE LPROD_GU = P_PROD_LGU;
     
     RETURN V_NAME;
END;     
/
SELECT PROD_ID, PROD_NAME, PROD_LGU, 
       FN_PRODNM(PROD_LGU) AS PROD_LGU
  FROM PROD;
